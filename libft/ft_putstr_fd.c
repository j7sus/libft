#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
}

/* ------------------------------------------------------------------------
 	La funcion putstr_fd con parametros dados 's' la string a enviar, 'fd'
   el file descriptor sobre el que eascribir.
   -----------------------------------------------------------------------*/
